#include "cube.h"

int	has_cub(char *s)
{
	size_t	ss;

	ss = ft_strlen(s);
	if (ss < 4)
		return (0);
	if (!ft_strcmp(".cub", s + ss - 4))
		return (1);
	return (0);
}
